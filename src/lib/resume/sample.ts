function csvCell(s: string): string {
  if (/[",\n]/.test(s)) return `"${s.replace(/"/g, '""')}"`;
  return s;
}

export function sampleCsvText(): string {
  const lines = [HEADER.map(csvCell).join(",")];
  for (const r of ROWS) lines.push(r.map(csvCell).join(","));
  return lines.join("\n");
}
