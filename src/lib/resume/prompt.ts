// Claude API 프롬프트 빌더 — 「지원 동기 및 포부」 작성용.
//
// 핵심 시점 가정:
//   - 작성하려는 본문 = 지원서 시점 (과거).
//   - 첨부 자료 = 지원서 제출 이후에 실제로 수행한 활동 기록 (예: 자문서, 활용내역서).
//   ⇒ 첨부의 사건을 그대로 과거형으로 본문에 옮기면 시간 모순.
//   ⇒ 첨부에서는 「전문 분야·관심·역량 단서」만 추출하여, "이런 역량을 쌓아왔다 →
//      앞으로 이렇게 기여하겠다" 미래형 톤으로 풀어쓰기.

import type { ResumeBasic, ResumeKind } from "@/lib/resume/types";

const KIND_BLURB: Record<ResumeKind, { title: string; tone: string }> = {
  coordinator: {
    title: "코디네이터 지원서",
    tone:
      "사업 및 정책 이해와 성과관리 역량이 드러나도록. 학교 디지털 전환·찾아가는 컨설팅 등 정부사업 맥락을 자연스럽게 녹여내기.",
  },
  instructor: {
    title: "강사 지원서",
    tone:
      "수업설계와 수업자료 개발 역량이 드러나도록. 학생·교원 대상 디지털 교육 경험과 그로 인한 성과를 평이하게 서술.",
  },
};

export function buildMotivationPrompt(input: {
  kind: ResumeKind;
  basic: ResumeBasic;
  attachedText: string;
}): string {
  const { kind, basic, attachedText } = input;
  const blurb = KIND_BLURB[kind];
  const profile = [
    basic.name && `성명: ${basic.name}`,
    basic.organization && `소속: ${basic.organization}`,
    basic.position && `직위/직책: ${basic.position}`,
    basic.gender && `성별: ${basic.gender}`,
    basic.birth && `생년월일: ${basic.birth}`,
  ]
    .filter(Boolean)
    .join(" / ");

  const ref = (attachedText || "").trim().slice(0, 12000);
  const hasRef = ref.length > 0;

  const lines: string[] = [
    `당신은 한국의 디지털 교육 사업(찾아가는 학교 컨설팅, 디지털 교실혁명 등)에서 활동하는 교사·코디네이터·자문위원의 지원서를 대신 작성해 주는 한국어 작문 전문가입니다.`,
    ``,
    `이 지원서의 양식: ${blurb.title}`,
    `이 칸의 톤: ${blurb.tone}`,
    ``,
    `[지원자 프로필]`,
    profile || "(프로필 정보가 비어있음)",
    ``,
  ];

  if (hasRef) {
    lines.push(
      `[참고 자료]`,
      ref,
      ``,
      `※ 매우 중요한 시점 안내:`,
      `- 위 「참고 자료」는 지원자가 본 사업에 합격한 이후 실제로 수행한 활동(자문서·활용내역서·결과보고 등) 기록입니다.`,
      `- 작성하려는 「지원 동기 및 포부」 본문은 사업 합격 이전 시점(지원서 제출 시점)에 작성된 것처럼 표현되어야 합니다.`,
      `- 따라서 참고 자료의 구체적 사건(특정 학교명·일자·금액·수상·연수 차시 등)을 본문에 그대로 과거형 사실로 옮겨 적지 마세요. 시간 모순이 됩니다.`,
      `- 대신 자료를 통해 알 수 있는 「지원자의 전문 분야·관심 영역·기존부터 보유한 역량의 단서」만 일반화·추상화하여 사용하세요.`,
      `- 본문 톤은 "그동안 ~ 분야에서 역량을 쌓아왔으며" + "앞으로 본 사업에서 ~ 기여하고자 합니다" 형태의 미래형 포부로 작성하세요.`,
    );
  } else {
    lines.push(
      `[참고 자료]`,
      `(첨부 없음 — 지원자 프로필만으로 일반적이고 자연스러운 자기소개를 작성)`,
      ``,
      `※ 첨부 자료가 없으므로:`,
      `- 지원자 프로필(소속·직위·교사 신분)에서 합리적으로 추론할 수 있는 일반적인 디지털 교육 관심·역량을 자연스럽게 서술하세요.`,
      `- 구체 사실을 만들어내지 말고, "디지털 대전환기 학교 현장에서 학생들의 미래 역량을 키우고자 한다", "교원으로서 디지털 도구를 수업에 통합한 경험을 바탕으로 ${blurb.title.replace(" 지원서", "")} 역할에 기여하고자 한다" 류의 보편적이지만 진정성 있는 포부로 작성하세요.`,
    );
  }

  lines.push(
    ``,
    `[작성 요구사항]`,
    `- 4~7문장, 200~400자 분량.`,
    `- 한국어 격식체 ("~합니다", "~하였습니다" 류 보고 어조).`,
    `- 마크다운 기호(*, #, - 등) 또는 머리말("아래는…", "다음과 같이…")을 쓰지 말 것. 본문만 출력.`,
    `- 첨부 자료에 명시되지 않았거나 추론 불가한 사실(수상·구체 학교명·일자·금액 등)을 새로 만들지 말 것.`,
    `- 첫 문장은 "디지털"·"교육"·"학교"·"수업" 등으로 자연스럽게 도입.`,
    `- 마지막 문장은 향후 본 사업에서의 활동에 대한 미래형 포부로 마무리.`,
  );

  return lines.join("\n");
}
